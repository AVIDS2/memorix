export interface OfficialMemorixSkill {
  name: string;
  content: string;
}

function skill(name: string, description: string, body: string[]): OfficialMemorixSkill {
  return {
    name,
    content: [
      '---',
      `name: ${name}`,
      `description: ${description}`,
      '---',
      '',
      ...body,
      '',
    ].join('\n'),
  };
}

